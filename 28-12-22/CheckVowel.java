package afternoon;

public class CheckVowel {

	public static void main(String[] args) {
		
		char ch = 'e';
		
		if((ch=='a')||(ch=='A')||(ch=='e')||(ch=='E')||(ch=='i')||(ch=='I')||(ch=='o')||(ch=='O')||(ch=='u')||(ch=='U'))
			System.out.print(ch+ " is a vowel");
		else
			System.out.print(ch+ " is not a vowel");

	}

}
